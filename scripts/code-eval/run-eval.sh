#!/usr/bin/env bash
# Run the code-intel evaluation battery over the fetched corpus.
#
# For each corpus repo (must be fetched first via fetch-corpus.sh):
#   1. fresh index (timed, with a per-repo timeout so a perf cliff can't hang the run)
#   2. a battery of challenging `atomic code` commands, output captured
#   3. a structural metrics.json (timing + counts + kind distribution + routes)
# Then a top-level SUMMARY.md table.
#
# Indexing THROUGHPUT is a first-class metric — index_seconds + files/sec + timed_out
# are recorded per repo so a perf regression (or improvement) is visible at a glance.
#
# Usage:
#   scripts/code-eval/run-eval.sh [id...]              # all fetched, or only given ids
#   INDEX_TIMEOUT=300 scripts/code-eval/run-eval.sh    # override per-repo index cap (default 180s)
#
# Output: tmp/code-eval/out/<id>/{metrics.json,commands.txt} + tmp/code-eval/out/SUMMARY.md
#
# Reproducibility: metrics record the repo's resolved SHA (from fetch). Pin the corpus
# (tags in corpus.tsv) and the structural metrics become stable golden candidates.

set -u

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
MANIFEST="$SCRIPT_DIR/corpus.tsv"
REPOS="$REPO_ROOT/tmp/code-eval/repos"
OUT="$REPO_ROOT/tmp/code-eval/out"
BIN="$REPO_ROOT/bin/atomic"
# INDEX_TIMEOUT is the FLOOR for the per-repo index cap (small repos). The
# effective cap scales with repo size: max(INDEX_TIMEOUT, files/INDEX_FILE_DIV).
# The engine itself has NO internal timeout — this cap only stops the eval run
# from hanging. Huge repos (e.g. postgres ~4k files) get a proportionally larger
# budget so a legitimately long index isn't reported as a perf cliff.
INDEX_TIMEOUT="${INDEX_TIMEOUT:-180}"
INDEX_FILE_DIV="${INDEX_FILE_DIV:-2}"   # ~0.5s/file budget above the floor
CMD_TIMEOUT=30

mkdir -p "$OUT"

# timeout binary: prefer gtimeout (macOS coreutils), fall back to timeout (linux)
TIMEOUT="$(command -v gtimeout || command -v timeout || true)"

# build the binary if missing
if [ ! -x "$BIN" ]; then
  echo "building bin/atomic (CGO_ENABLED=0)…"
  ( cd "$REPO_ROOT/atomic" && CGO_ENABLED=0 go build -o ../bin/atomic ./cmd/atomic ) || {
    echo "build failed"; exit 1; }
fi

want=("$@")
wanted() {
  [ ${#want[@]} -eq 0 ] && return 0
  local id="$1"; local w
  for w in "${want[@]}"; do [ "$w" = "$id" ] && return 0; done
  return 1
}

# run a labeled command, append to commands.txt with timing + exit + (capped) output
runcmd() {
  local cmds="$1"; local label="$2"; shift 2
  local s e rc
  { echo; echo "### $label"; echo "\$ atomic --repo <repo> ${*}"; } >> "$cmds"
  s=$(date +%s)
  if [ -n "$TIMEOUT" ]; then
    "$TIMEOUT" "$CMD_TIMEOUT" "$BIN" --repo "$DIR" "$@" >> "$cmds" 2>&1; rc=$?
  else
    "$BIN" --repo "$DIR" "$@" >> "$cmds" 2>&1; rc=$?
  fi
  e=$(date +%s)
  echo "[exit $rc, $((e-s))s]" >> "$cmds"
}

summary="$OUT/SUMMARY.md"
{
  echo "# Code-intel eval summary"
  echo
  echo "Per-repo index throughput + extraction counts. Generated by run-eval.sh."
  echo "Per-repo index cap = max(${INDEX_TIMEOUT}s floor, files/${INDEX_FILE_DIV}s). \`timed_out=yes\` = did not finish within that cap (the harness cap, not an engine limit)."
  echo
  echo "| id | kind | lang | files | nodes | edges | routes | index_s | files/s | timed_out |"
  echo "|----|------|------|------|------|------|------|--------|---------|-----------|"
} > "$summary"

while IFS=$'\t' read -r id kind language url ref note; do
  case "$id" in ''|\#*) continue;; esac
  wanted "$id" || continue
  DIR="$REPOS/$id"
  [ -d "$DIR/.git" ] || { echo "skip $id (not fetched)"; continue; }
  odir="$OUT/$id"; mkdir -p "$odir"
  cmds="$odir/commands.txt"; : > "$cmds"
  sha="$(cat "$REPOS/$id.sha" 2>/dev/null || echo unknown)"
  echo "=== $id ($language, $kind) @ $sha ==="

  # fresh index, timed, capped. Cap scales with repo size (floor + per-file budget).
  rm -rf "$DIR/.claude/.atomic-index"
  nfiles=$(git -C "$DIR" ls-files 2>/dev/null | wc -l | tr -d ' ')
  cap=$(( ${nfiles:-0} / INDEX_FILE_DIV ))
  [ "$cap" -lt "$INDEX_TIMEOUT" ] && cap=$INDEX_TIMEOUT
  s=$(date +%s)
  if [ -n "$TIMEOUT" ]; then
    "$TIMEOUT" "$cap" "$BIN" --repo "$DIR" code index > "$odir/index.txt" 2>&1; irc=$?
  else
    "$BIN" --repo "$DIR" code index > "$odir/index.txt" 2>&1; irc=$?
  fi
  e=$(date +%s); isec=$((e-s))
  timed_out=no; [ $irc -eq 124 ] && timed_out=yes
  echo "  index: ${isec}s (cap=${cap}s, files=${nfiles}, rc=$irc, timed_out=$timed_out)"

  # structural metrics from status --json (works even if index partial — DB has what landed)
  "$BIN" --repo "$DIR" code status --json > "$odir/status.json" 2>/dev/null || true
  metrics="$(python3 - "$odir/status.json" "$id" "$kind" "$language" "$sha" "$isec" "$timed_out" <<'PY'
import json,sys
path,id_,kind,lang,sha,isec,timed=sys.argv[1:8]
try:
    d=json.load(open(path))
except Exception:
    d={}
files=d.get("fileCount",0); nodes=d.get("nodeCount",0); edges=d.get("edgeCount",0)
kinds=d.get("nodesByKind",{}) or {}
routes=kinds.get("route",0)
isec=int(isec); fps=round(files/isec,1) if isec>0 else 0
m={"id":id_,"kind":kind,"language":lang,"sha":sha,"index_seconds":isec,
   "timed_out":timed=="yes","files":files,"nodes":nodes,"edges":edges,
   "routes":routes,"files_per_sec":fps,"nodesByKind":kinds}
json.dump(m,open(path.replace("status.json","metrics.json"),"w"),indent=2,sort_keys=True)
print(f"{files}\t{nodes}\t{edges}\t{routes}\t{fps}")
PY
)"
  files=$(echo "$metrics" | cut -f1); nodes=$(echo "$metrics" | cut -f2)
  edges=$(echo "$metrics" | cut -f3); routes=$(echo "$metrics" | cut -f4); fps=$(echo "$metrics" | cut -f5)

  # the challenge battery — exercises the full CLI surface on real code.
  # (flags BEFORE the positional symbol — current CLI requirement; finding B.)
  runcmd "$cmds" "status (json)" code status --json
  runcmd "$cmds" "files" code files
  runcmd "$cmds" "search functions" code search "kind:function"
  runcmd "$cmds" "search methods in this lang" code search "kind:method lang:$language"
  runcmd "$cmds" "search routes" code search "kind:route"
  runcmd "$cmds" "search term: handler" code search "handler"

  # derive a real symbol from the index (first function name) and exercise the graph verbs
  SYM="$("$BIN" --repo "$DIR" code search "kind:function" 2>/dev/null | awk 'NR==1{print $2}')"
  if [ -n "${SYM:-}" ]; then
    echo "  sample symbol: $SYM"
    runcmd "$cmds" "node $SYM"            code node "$SYM"
    runcmd "$cmds" "callers $SYM (d1)"    code callers --depth 1 "$SYM"
    runcmd "$cmds" "callees $SYM (d2)"    code callees --depth 2 "$SYM"
    runcmd "$cmds" "impact $SYM (d3)"     code impact  --depth 3 "$SYM"
    runcmd "$cmds" "explore $SYM"         code explore "$SYM"
  else
    echo "  (no functions found — skipping symbol battery)" >> "$cmds"
  fi
  runcmd "$cmds" "explore: authentication" code explore "authentication"

  # affected: feed the first indexed file via --stdin
  FIRST="$("$BIN" --repo "$DIR" code files 2>/dev/null | awk 'NR==1{print $1}')"
  if [ -n "${FIRST:-}" ]; then
    { echo; echo "### affected (stdin: $FIRST)"; } >> "$cmds"
    echo "$FIRST" | "$BIN" --repo "$DIR" code affected --stdin >> "$cmds" 2>&1
    echo "[exit $?]" >> "$cmds"
  fi

  printf '| %s | %s | %s | %s | %s | %s | %s | %s | %s | %s |\n' \
    "$id" "$kind" "$language" "$files" "$nodes" "$edges" "$routes" "$isec" "$fps" "$timed_out" >> "$summary"
done < "$MANIFEST"

echo
echo "=== SUMMARY ($summary) ==="
cat "$summary"
